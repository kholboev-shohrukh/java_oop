package oop;

public class MathFunctions {

    static int add(int a, int b){
        return a + b;
    }
    static int sub(int a, int b){
        return a - b;
    }
    static int multiply(int a, int b){
        return a * b;
    }
    static double div(double a, double b){
        return a / b;
    }
    static double abs(double a){
        return Math.abs(a);
    }
    static double pow(double a){
        return a * a;
    }
}
